def sum(lista):
    """
    Esta funcion recibe una lista de numeros y devuelve la suma de todos los elementos.
    
    Args:
    lista (list): Una lista de numeros.
    
    Returns:
    float: La suma de todos los numeros en la lista.
    """
    total = 0
    for num in lista:
        total += num
    return total

def multip(lista):
    """
    Esta funcion recibe una lista de numeros y devuelve el producto de todos los elementos.
    
    Args:
    lista (list): Una lista de numeros.
    
    Returns:
    float: El producto de todos los numeros en la lista.
    """
    total = 1
    for num in lista:
        total *= num
    return total


  